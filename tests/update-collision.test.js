test('install never touches a user Harness/tasks/** file (not a write path; preserved untouched)', () => {
  // Harness/tasks/** is preserve-only; user task files are not installer write
  // paths and must never appear in any plan write/overwrite bucket.
  const root = tmpdir();
  const targetDir = path.join(root, 'tasks-preserve');
  const userTask = '# My task progress\nReal user task data.\n';
  writeExisting(targetDir, 'Harness/tasks/my-task/PROGRESS.md', userTask);

  const result = generate({ projectName: 'tasks-preserve', targetDir, onConflict: 'overwrite' });

  assert.equal(result.success, true);
  const allPlanned = [
    ...result.plan.create,
    ...result.plan.overwrite,
    ...result.plan.backup,
    ...result.plan.skip,
    ...result.plan.conflict,
  ];
  assert.equal(allPlanned.includes('Harness/tasks/my-task/PROGRESS.md'), false);
  assert.equal(
    fs.readFileSync(path.join(targetDir, 'Harness/tasks/my-task/PROGRESS.md'), 'utf8'),
    userTask,
  );
});

